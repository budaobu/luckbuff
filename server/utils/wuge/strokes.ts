export function getStrokeCount(char: string): number | null {
  return STROKE_MAP[char] ?? null
}

export function getNameStrokeCounts(name: string): { char: string; strokes: number | null }[] {
  const result: { char: string; strokes: number | null }[] = []
  for (const c of name) {
    result.push({ char: c, strokes: getStrokeCount(c) })
  }
  return result
}

export function hasUnknownStrokes(name: string): { unknown: string[]; known: { char: string; strokes: number }[] } {
  const unknown: string[] = []
  const known: { char: string; strokes: number }[] = []
  for (const c of name) {
    const s = getStrokeCount(c)
    if (s === null) {
      unknown.push(c)
    } else {
      known.push({ char: c, strokes: s })
    }
  }
  return { unknown, known }
}

// 笔画缓存（本地 / 转换 / AI 结果均会缓存，工作节点重启后清空）
const strokeCache = new Map<string, number>()

function normalizeStrokeValue(value: unknown): number | null {
  let num: number | undefined
  if (typeof value === 'number') {
    num = value
  } else if (typeof value === 'string') {
    num = parseInt(value.trim(), 10)
  }
  if (num === undefined || Number.isNaN(num)) return null
  if (!Number.isInteger(num) || num <= 0 || num > 200) return null
  return num
}

// 延迟初始化 opencc-js + cnchar，只在真正需要时才加载
let openccConverter: ((text: string) => string) | null = null
let cncharModule: { stroke: (char: string) => number } | null = null

async function initStrokeLibraries(): Promise<{ converter: (text: string) => string; cnchar: { stroke: (char: string) => number } }> {
  if (openccConverter && cncharModule) {
    return { converter: openccConverter, cnchar: cncharModule }
  }

  const [{ default: OpenCC }, { default: cnchar }, { default: trad }] = await Promise.all([
    import('opencc-js'),
    import('cnchar'),
    import('cnchar-trad'),
  ])

  cnchar.use(trad)
  openccConverter = OpenCC.Converter({ from: 'cn', to: 'twp' }) as (text: string) => string
  cncharModule = cnchar as { stroke: (char: string) => number }

  return { converter: openccConverter, cnchar: cncharModule }
}

async function lookupStrokesWithConverter(char: string): Promise<number | null> {
  try {
    const { converter, cnchar } = await initStrokeLibraries()
    const traditional = converter(char)
    const count = cnchar.stroke(traditional)
    return normalizeStrokeValue(count)
  } catch {
    return null
  }
}

/**
 * 批量解析汉字笔画数。
 * 优先级：本地静态表 > opencc-js（简转繁）+ cnchar 笔画 > AI API 兜底。
 */
export async function getStrokeCountsWithAIFallback(chars: string[]): Promise<Record<string, number>> {
  const uniqueChars = [...new Set(chars.filter(Boolean))]
  const result: Record<string, number> = {}
  const needLookup: string[] = []

  for (const char of uniqueChars) {
    const cached = strokeCache.get(char)
    if (cached !== undefined) {
      result[char] = cached
      continue
    }
    const local = STROKE_MAP[char]
    if (local !== undefined) {
      result[char] = local
      continue
    }
    needLookup.push(char)
  }

  if (needLookup.length === 0) return result

  // 第二步：opencc-js 简转繁（twp）后用 cnchar 取笔画
  const needAI: string[] = []
  for (const char of needLookup) {
    const strokes = await lookupStrokesWithConverter(char)
    if (strokes !== null) {
      strokeCache.set(char, strokes)
      result[char] = strokes
    } else {
      needAI.push(char)
    }
  }

  if (needAI.length === 0) return result

  // 第三步：AI 兜底（需要配置 aiApiKey）
  const config = useRuntimeConfig()
  if (!config.aiApiKey) {
    throw createError({
      statusCode: 400,
      statusMessage: `以下字的笔画数暂未收录，请换字：${needAI.join('、')}`,
    })
  }

  const system = `你是一位精通《康熙字典》笔画数的汉字专家。
用户会提供若干汉字（可能是简体、繁体或异体）。
请按繁体/康熙字典标准计算每个字的笔画数，并只返回一个纯 JSON 对象：
{
  "龍": 16,
  "飛": 9
}
要求：
- 键必须是用户输入的每个原字；
- 值是正整数笔画数；
- 不要 Markdown、不要解释、不要多余字段。`

  const user = `请计算以下汉字的康熙字典笔画数：${needAI.join('、')}`
  const parsed = await callAIJson(system, user)

  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    throw createError({ statusCode: 500, statusMessage: 'AI 笔画查询返回格式错误' })
  }

  const mapping = parsed as Record<string, unknown>
  const stillUnknown: string[] = []

  for (const char of needAI) {
    const value = mapping[char]
    const strokes = normalizeStrokeValue(value)
    if (strokes === null) {
      stillUnknown.push(char)
      continue
    }
    strokeCache.set(char, strokes)
    result[char] = strokes
  }

  if (stillUnknown.length > 0) {
    throw createError({
      statusCode: 400,
      statusMessage: `以下字的笔画数暂未收录，请换字：${stillUnknown.join('、')}`,
    })
  }

  return result
}

