// 爻数组（6位二进制，0=阴爻，1=阳爻）
export function getYaoArray(gua: GuaInfo): number[] {
  // 上卦和下卦转二进制爻
  // 八卦序号 1-8 对应：乾111, 兑110, 离101, 震100, 巽011, 坎010, 艮001, 坤000
  const baguaBinary: Record<number, number[]> = {
    1: [1, 1, 1], // 乾
    2: [1, 1, 0], // 兑
    3: [1, 0, 1], // 离
    4: [1, 0, 0], // 震
    5: [0, 1, 1], // 巽
    6: [0, 1, 0], // 坎
    7: [0, 0, 1], // 艮
    8: [0, 0, 0], // 坤
  }
  const shang = baguaBinary[gua.shangGua] || [0, 0, 0]
  const xia = baguaBinary[gua.xiaGua] || [0, 0, 0]
  // 下卦在前（初爻到三爻），上卦在后（四爻到六爻）
  return [...xia, ...shang]
}

// 根据爻数组查找卦
export function findGuaByYao(yaoArray: number[]): GuaInfo | undefined {
  const baguaBinary: Record<string, number> = {
    '1,1,1': 1, '1,1,0': 2, '1,0,1': 3, '1,0,0': 4,
    '0,1,1': 5, '0,1,0': 6, '0,0,1': 7, '0,0,0': 8,
  }
  const xiaId = baguaBinary[yaoArray.slice(0, 3).join(',')]
  const shangId = baguaBinary[yaoArray.slice(3, 6).join(',')]
  return LIUSHISI_GUA.find(g => g.xiaGua === xiaId && g.shangGua === shangId)
}
